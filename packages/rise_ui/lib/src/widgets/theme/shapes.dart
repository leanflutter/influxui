enum Shape {
  /// a four-sided flat shape with straight sides and four right angles
  rectangle,

  /// a four-sided shape with sides of equal length and four right angles
  square,

  /// a round plane shape whose boundary consists of points equidistant from a fixed point
  circle,

  /// a shape with curves instead of straight lines, with no sharp angles
  round,

  /// a shape similar to a flat oval, with slightly flattened or rounded ends
  pill,
}
